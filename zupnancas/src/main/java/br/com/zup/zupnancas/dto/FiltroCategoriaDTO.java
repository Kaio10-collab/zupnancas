package br.com.zup.zupnancas.dto;

public class FiltroCategoriaDTO {

    private String nome;
    private String cpf;

    public FiltroCategoriaDTO() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }
}